  <h1>Fotoredigering</h1>
  <article class="art1">
    <p>Til det her projekt var vi i nogle grupper, ude og tage billeder af broer i København.</p>
    <h2>Opgaven</h2>
    <p>Da vi kom tilbage til skolen, lærte vi lidt Photoshop, som vi skulle bruge til at redigere et af
      de billeder vi havde taget. Ifølge opgaven skulle vi:</p>
    <ul>
      <li>Fjerne et element</li>
      <li>Tilføje et element</li>
      <li>Rette billedets lysforhold</li>
      <li>Beskære og oprette billedet</li>
      <li>Rette format og størrelse til 3:4 og under 1Mpixel</li>
    </ul>
    <h2>Mine rettelser</h2>
    <p>Det første billede er det oprindelige billede, helt uden nogle rettelser.</p>
    <p>Jeg startede med at beskære billedet til formatet 3:4, som jeg havde fået besked på. På samme tid, rettede
      jeg billedet op, så broen var lige horizontalt.</p>
    <p>Herefter begyndte jeg at justere på lysforholdende, så billedet blev rare at se på, og farverne blev mere
      klare.</p>
    <p>Jeg fik fjernet nogle ting på kajen i forgrunden af billedet, og jeg fik fjernet klistermærker på broen.</p>
    <p>Der skulle jo tilføjes nogle ting til billedet også, og der valgte jeg at sætte en båd mere ind, som jeg
      malede for at de ikke var for identiske.Jeg tilføjede også en lille mand i en lyserød regn-poncho oppe på broen,
      og en ny mere klar og blå himmel.</p>
  </article>

