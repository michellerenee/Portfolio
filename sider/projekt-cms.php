  <h1>Mini CMS system</h1>
  <article class="art1">
    <p>I forbindelse med min svendeprøve som Webintegrator, lavede jeg et lille CMS system til at styre indholdet på
      den side jeg skulle lave til svendeprøven. Siden hed "Magasinet Bil, Båd & Bike". Derfor det står i venstre top
      hjørne</p>
    <p>Man kan se hvad der er af indhold på siden, og man kan styre det herfra. Det er et magasin, så der er
      artikler på siden. Fra systemet her, kan man oprette, rette og slette artiklerne. Det samme kan man med
      brugere.</p>
    <p>Man kan også se hvilke mailadresser der er tilmeldt nyhedsbreve, og se en logbog over alt hvad der er sket i
      CMS systemet.</p>
    <p>Man kan også aktivere og deaktivere artikler, brugere og kategorier.</p>
    <h2>Prøv løs</h2>
    <p>Hvis du har lyst til at prøve at klikke rundt på siden, slette og oprette lidt forskelligt. Klik på billedet
      til højre og log ind med:</p>
    <p>Email: <strong>cpe@bbbmag.dk</strong><br>
      Adgangskode: <strong>business</strong></p>
