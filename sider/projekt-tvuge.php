  <h1>Webdesign</h1>
  <article class="art1">
    <p>Det her var et projekt jeg syntes var rigtig sjovt at lave.</p>
    <p>Det var fra da jeg gik på webintegrator uddannelsen, hvor vi havde noget der blev kaldt Roskilde
      mesterskaberne.</p>
    <p>Til Roskilde mesterskaberne er der nogle forskellige kategorier man kunne deltage i. Der var Webdesign,
      programering for grundforløb, programering, 3D, og print. Vi havde valgt webdesign.</p>
    <h2>Opgaven</h2>
    <p>Til Roskilde mesterskaberne, arbejder man 2 dage fra 9-18. Den første dag får man en intro, og får så temaet
      at vide. Vores tema var Roskilde TV-seriefestival. Vores opgave var så, at lave et webdesign til Roskilde
      TV-serie festival.</p>
    <h2>Arbejdet</h2>
    <p>I min gruppe var vi 3 personer, og vi kaldte vores gruppe for Save For Web.</p>
    <p>Vi arbejde meget intens de fleste af timerne, begge dage. Vi hyggede os rigtig meget med at lave det. Vi
      hentede noget inspiration fra roskildefestival.dk og netflix.dk.</p>
    <p>Vi var faktisk rigtig stolte af vores færdige resultat, og vi endte også med at vinde konkurrencen for det
      bedste webdesign. Udover det, kom vi på 2. pladsen for det bedste produkt, af alt der blev lavet.</p>
    <p>Vi fik lavet et fuldt layout til alle de sider vi mente der skulle være der. Ud over det fik vi lavet
      layout til, hvordan det skulle se ud på de forskellige størrelser af skærme. Vores færdige resultat kan ses til
      højre.</p>
